    private void bInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInsertActionPerformed
        // TODO add your handling code here:
        Connection conexion = null;
        try {
            conexion = Conexion.getConnection();
            
            //el autocommit por default es true, lo pasamos a false
            if (conexion.getAutoCommit()) {
                conexion.setAutoCommit(false);
                jAprob.setText("Se ha conectado a la BD");
                System.out.println("SE HA CONECTADO");
            }

            SerieDaoJDBC serieoJdbc = new SerieDaoJDBC(conexion);

            SerieDTO nuevaSerie = new SerieDTO();
            nuevaSerie.setNombre(""+jNomS.getText());
            nuevaSerie.setAnio(Integer.parseInt(jAnio.getText()));
            nuevaSerie.setGenero(""+jGen.getText());
            nuevaSerie.setEpisodios(Integer.parseInt(jEpi.getText()));
            nuevaSerie.setAct1(""+jAct1.getText());
            nuevaSerie.setAct2(""+jAct2.getText());
            nuevaSerie.setAct3(""+jAct3.getText());
            nuevaSerie.setAct4(""+jAct4.getText());

            serieoJdbc.insert(nuevaSerie);
            
         conexion.commit();
            jTrans.setText("Se ha hecho la transaccion");
            System.out.println("Se ha hecho commit de la transaccion");
        } catch (SQLException ex) {
            ex.printStackTrace(System.out);
            System.out.println("Entramos al rollback");
            try {
                conexion.rollback();
            } catch (SQLException ex1) {
                ex1.printStackTrace(System.out);
            }
        }
        borrar();
    }//GEN-LAST:event_bInsertActionPerformed

